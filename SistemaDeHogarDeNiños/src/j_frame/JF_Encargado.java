    private void jButtonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarActionPerformed
        // TODO add your handling code here:
        int fila =jTablesEncargadoE.getSelectedRow();
        if (fila > -1){
            String ci = jTablesEncargadoE.getValueAt(fila, 0).toString();
            String Nombre = jTablesEncargadoE.getValueAt(fila, 1).toString();
            String Apellido = jTablesEncargadoE.getValueAt(fila, 2).toString();
            String sexo = jTablesEncargadoE.getValueAt(fila, 3).toString();
            String Tipo = jTablesEncargadoE.getValueAt(fila, 4).toString();
            String correo = jTablesEncargadoE.getValueAt(fila, 5).toString();
            String cel_ref = jTablesEncargadoE.getValueAt(fila, 6).toString();
            String Fecha_i = jTablesEncargadoE.getValueAt(fila, 7).toString();
            String fecha_f = jTablesEncargadoE.getValueAt(fila, 8) != null ? jTablesEncargadoE.getValueAt(fila, 8).toString() : "";

            jTextFielCIE.setText(ci);
            jTextFieldNOMBREE.setText(Nombre);
            jTextFieldAPELLIDOE.setText(Apellido);
            jTextFieldSEXOE.setText(sexo);
            jTextFieldTIPOE.setText(Tipo);
            jTextFieldCORREOE.setText(correo);
            jTextFieldCELULARE.setText(cel_ref);
            jTextFieldFECHA_IE.setText(Fecha_i);
            jTextFieldFECHA_FE.setText(fecha_f);
           
        }
    }//GEN-LAST:event_jButtonSeleccionarActionPerformed

