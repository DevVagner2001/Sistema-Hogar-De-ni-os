    private void jButtonSeleccionarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarIdActionPerformed
        int fila =jTablesEncargadoE.getSelectedRow();
        if (fila > -1){
           String id = jTablesEncargadoE.getValueAt(fila, 0).toString();
           if (JF_labor.OficialLabor!= null && JF_labor.OficialLabor.isVisible()) {
            JF_labor.OficialLabor.jTextFielciDencargado.setText(id);
            JF_Encargado.jButtonNuevoE.setEnabled(true);
            JF_Encargado.jButtonModificarE.setEnabled(true);
            JF_Encargado.jButtonguardarE.setEnabled(true);
            JF_Encargado.jButtonEliminarE.setEnabled(true);
            JF_Encargado.jButtonSeleccionar.setEnabled(true);
            JF_Encargado.jButton2.setEnabled(true);
            JF_Encargado.jLabeltxtencargado.setText("");
           }
           
        }
         this.dispose();
    }//GEN-LAST:event_jButtonSeleccionarIdActionPerformed

