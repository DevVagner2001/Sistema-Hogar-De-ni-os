    private void jButtonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarActionPerformed
        // TODO add your handling code here:
        int fila =jTablesECRETARIA.getSelectedRow();
        if (fila > -1){
            String idP = jTablesECRETARIA.getValueAt(fila, 0).toString();
            String Nombre = jTablesECRETARIA.getValueAt(fila, 1).toString();
            String Apellido = jTablesECRETARIA.getValueAt(fila, 2).toString();
            String sexo = jTablesECRETARIA.getValueAt(fila, 3).toString();
            String Tipo = jTablesECRETARIA.getValueAt(fila, 4).toString();
            String correo = jTablesECRETARIA.getValueAt(fila, 5).toString();
            String cel_ref = jTablesECRETARIA.getValueAt(fila, 6).toString();
            String Fecha_i = jTablesECRETARIA.getValueAt(fila, 7).toString();
            String fecha_f = jTablesECRETARIA.getValueAt(fila, 8) != null ? jTablesECRETARIA.getValueAt(fila, 8).toString() : "";
            String velocidad = jTablesECRETARIA.getValueAt(fila, 9).toString();
            
        jTextFielCI.setText(idP);
        jTextFieldNOMBRE.setText(Nombre);
        jTextFieldAPELLIDO.setText(Apellido);
        jTextFieldSEXO.setText(sexo);
        jTextFieldTIPO.setText(Tipo);
        jTextFieldCORREO.setText(correo);
        jTextFieldCELULAR.setText(cel_ref);
        jTextFieldFECHA_I.setText(Fecha_i);
        jTextFieldFECHA_F.setText(fecha_f);
        jTextFielVELOCIDAD_T.setText(velocidad);
        }
    }//GEN-LAST:event_jButtonSeleccionarActionPerformed

