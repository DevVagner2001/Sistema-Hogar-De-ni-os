    private void jButtonSeleccionarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarIdActionPerformed
        int fila =jTablesECRETARIA.getSelectedRow();
        if (fila > -1){
            String id = jTablesECRETARIA.getValueAt(fila, 0).toString();
            if(JF_niños.niñoss!=null && JF_niños.niñoss.isVisible()){
                JF_niños.niñoss.jTextIDCIsecre.setText(id);
                JF_Secretaria.jButtonatras.setEnabled(true);
                JF_Secretaria.jButtonNuevo.setEnabled(true);
                JF_Secretaria.jButtonModificar.setEnabled(true);
                JF_Secretaria.jButtonguardar.setEnabled(true);
                JF_Secretaria.jButtonEliminar.setEnabled(true);
                JF_Secretaria.jButtonSeleccionar.setEnabled(true);
                JF_Secretaria.jLabelSecretaria.setText("");
            }
            if(JF_Ingreso.OficialIngreso!=null&&JF_Ingreso.OficialIngreso.isVisible()){
                JF_Ingreso.OficialIngreso.jTextFieldCisecre.setText(id);
                JF_Secretaria.jButtonNuevo.setEnabled(true);
                JF_Secretaria.jButtonModificar.setEnabled(true);
                JF_Secretaria.jButtonguardar.setEnabled(true);
                JF_Secretaria.jButtonEliminar.setEnabled(true);
                JF_Secretaria.jButtonatras.setEnabled(true);
                JF_Secretaria.jButtonSeleccionar.setEnabled(true);
                JF_Secretaria.jLabelSecretaria.setText("");
            }
        }
                 
         this.dispose();
    }//GEN-LAST:event_jButtonSeleccionarIdActionPerformed

