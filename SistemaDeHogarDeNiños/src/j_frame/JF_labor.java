    public void LogearLabor(){
        switch(JF_principal.valiladorPrincipal){
            case"1":
                JF_labor.jButtonjfarea.setEnabled(false);
                JF_labor.jButtonjfniños.setEnabled(false);
                JF_labor.jButtonjfencargado.setEnabled(false);
                JF_labor.jButtonNuevo.setEnabled(false);
                JF_labor.jButtonGuardar.setEnabled(false);
                JF_labor.jButtonModificar.setEnabled(false);
                JF_labor.jButtonEliminar.setEnabled(false);
                JF_labor.jButtonSeleccionar.setEnabled(false);
                break;
            case"2":
                break;
            case"3":
            case"4":
                JF_labor.jButtonEliminar.setEnabled(false);
                break;
            default:
                break;               
        }
    }
