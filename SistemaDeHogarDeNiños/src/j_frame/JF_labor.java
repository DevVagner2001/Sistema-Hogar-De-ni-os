    private void jButtonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarActionPerformed
        // TODO add your handling code here:
        int fila =jTablelabor.getSelectedRow();
        if (fila > -1){
            String idP = jTablelabor.getValueAt(fila, 0).toString();
            String fecha_i = jTablelabor.getValueAt(fila, 1).toString();
            String fecha_f = (jTablelabor.getValueAt(fila, 2) != null) ? jTablelabor.getValueAt(fila, 2).toString() : "";
            String descripcion = jTablelabor.getValueAt(fila, 3).toString();
            String idarea = jTablelabor.getValueAt(fila, 4).toString();
            String idniños = jTablelabor.getValueAt(fila, 5).toString();
            String idencargado = jTablelabor.getValueAt(fila, 6).toString();
           
            jTextFieldID.setText(idP);
            jTextFieldFechaI.setText(fecha_i);
            jTextFieldFechaF.setText(fecha_f);
            jTextFielDescripcion.setText(descripcion);
            jTextFieldIDarea.setText(idarea);
            jTextFieldIDniños.setText(idniños);
            jTextFielciDencargado.setText(idencargado);
        }
    }//GEN-LAST:event_jButtonSeleccionarActionPerformed

