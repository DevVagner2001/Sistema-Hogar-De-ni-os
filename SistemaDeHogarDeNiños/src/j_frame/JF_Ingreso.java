    public void LogearIngres(){
        switch(JF_principal.valiladorPrincipal){
            case"1":
                JF_Ingreso.jButtonSecretaria.setEnabled(false);
                JF_Ingreso.jButtonTipoIngreso.setEnabled(false);
                JF_Ingreso.jButtonNuevo.setEnabled(false);
                JF_Ingreso.jButtonGuardar.setEnabled(false);
                JF_Ingreso.jButtonModificar.setEnabled(false);
                JF_Ingreso.jButtonEliminar.setEnabled(false);
                JF_Ingreso.jButtonSeleccionar.setEnabled(false);    
                break;
            case"2":
                break;
            case"3":
            case"4":
                JF_Ingreso.jButtonEliminar.setEnabled(false);
                break;
            default:
                break;               
        }
    }
