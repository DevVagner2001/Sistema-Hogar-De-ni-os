    private void jButtonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarActionPerformed
        // TODO add your handling code here:
        int fila =jTableIngreso.getSelectedRow();
        if (fila > -1){
            String idi = jTableIngreso.getValueAt(fila, 0).toString();
            String Monto = jTableIngreso.getValueAt(fila, 1).toString();
            String fecha = jTableIngreso.getValueAt(fila, 2).toString();
            String Descri = (jTableIngreso.getValueAt(fila, 3) != null) ? jTableIngreso.getValueAt(fila, 3).toString() : "";
            String ids = jTableIngreso.getValueAt(fila, 4).toString();
            String idti = jTableIngreso.getValueAt(fila, 5).toString();
            
            jTextFieldID.setText(idi);
            jTextFieldMonto.setText(Monto);
            jTextFielFecha.setText(fecha);
            jTextFieldDescripcion.setText(Descri);
            jTextFieldCisecre.setText(ids);
            jTextFieldIDIngresooo.setText(idti);
         
        }
    }//GEN-LAST:event_jButtonSeleccionarActionPerformed

