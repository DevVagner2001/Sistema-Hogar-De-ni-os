    private void jButtonSeleccioarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccioarActionPerformed
        // TODO add your handling code here:
          int fila =jtableUE.getSelectedRow();
        if (fila > -1){
            String idUE = jtableUE.getValueAt(fila, 0).toString();
            String Nombre = jtableUE.getValueAt(fila, 1).toString();
            String Direccion = jtableUE.getValueAt(fila, 2).toString();
            String cel_ref = jtableUE.getValueAt(fila, 3).toString();
            
            jTextFiedID.setText(idUE);
            jTextFieldNombre.setText(Nombre);
            jTextFieldDireccion.setText(Direccion);
            jTextFieldcel.setText(cel_ref);
        }
        
    }//GEN-LAST:event_jButtonSeleccioarActionPerformed

    private void jButtonseleIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonseleIDActionPerformed
        int fila =jtableUE.getSelectedRow();
        if (fila > -1){
           String id = jtableUE.getValueAt(fila, 0).toString();
           if (JF_formacion.OficialFormacion != null && JF_formacion.OficialFormacion.isVisible()) {
            JF_formacion.OficialFormacion.jTextFieldIDUE.setText(id);
            JF_UE.jButtonnuevo.setEnabled(true);
            JF_UE.jButtonGuardar.setEnabled(true);
            JF_UE.jButtonModificar.setEnabled(true);
            JF_UE.jButtonEliminarrrr.setEnabled(true);
            JF_UE.jButton2.setEnabled(true);
            JF_UE.jButtonSeleccioar.setEnabled(false);
            JF_UE.jLabel7.setText("");
        }
            if (JF_principal.operaciones != null && JF_principal.operaciones.isVisible()) {
            JF_principal.operaciones.jTextFieldIDUE.setText(id);
        }
           
           this.dispose();
        } 
    }//GEN-LAST:event_jButtonseleIDActionPerformed

