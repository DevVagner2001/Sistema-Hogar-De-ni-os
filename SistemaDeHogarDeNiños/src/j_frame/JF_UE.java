    public void LogearUE(){
        switch(JF_principal.valiladorPrincipal){
            case"1":
                JF_UE.jButtonnuevo.setEnabled(false);
                JF_UE.jButtonGuardar.setEnabled(false);
                JF_UE.jButtonModificar.setEnabled(false);
                JF_UE.jButtonEliminarrrr.setEnabled(false);
                JF_UE.jButtonseleID.setEnabled(false);
                JF_UE.jButtonSeleccioar.setEnabled(false);
                break;
            case"2":
                break;
            case"3":
            case"4":
                JF_UE.jButtonEliminarrrr.setEnabled(false);
                break;
            default:
                break;               
        }
    }
