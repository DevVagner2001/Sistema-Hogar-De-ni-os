    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int fila =Tabla_Personal.getSelectedRow();
        if (fila > -1){
           String id = Tabla_Personal.getValueAt(fila, 0).toString();
           if (JF_usuario.OficialUsu != null && JF_usuario.OficialUsu.isVisible()) {
           JF_usuario.OficialUsu.jTextFieldIDPERSONAL.setText(id);
           JF_Personal.jButtonNuevo.setEnabled(true);
           JF_Personal.jButtonGuardar.setEnabled(true);
           JF_Personal.jButtonEliminar.setEnabled(true);
           JF_Personal.jButtonModificar.setEnabled(true);
           JF_Personal.jButtonSeleccionar.setEnabled(true);
           JF_Personal.jButtonatras.setEnabled(true);
           JF_Personal.jButton1.setVisible(false);
           }
        }
         this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

