    private void jButtonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSeleccionarActionPerformed
        
        int fila =Tabla_Personal.getSelectedRow();
        if (fila > -1){
            String idP = Tabla_Personal.getValueAt(fila, 0).toString();
            String Nombre = Tabla_Personal.getValueAt(fila, 1).toString();
            String Apellido = Tabla_Personal.getValueAt(fila, 2).toString();
            String sexo = Tabla_Personal.getValueAt(fila, 3).toString();
            String Tipo = Tabla_Personal.getValueAt(fila, 4).toString();
            String correo = Tabla_Personal.getValueAt(fila, 5).toString();
            String cel_ref = Tabla_Personal.getValueAt(fila, 6).toString();
            String Fecha_i = Tabla_Personal.getValueAt(fila, 7).toString();
            String fecha_f = Tabla_Personal.getValueAt(fila, 8).toString()!= null ? Tabla_Personal.getValueAt(fila, 8).toString() : "";
            
            jt_ci.setText(idP);
            jt_nombre.setText(Nombre);
            jt_Apellido.setText(Apellido);
            jt_sexo.setText(sexo);
            jt_Tipo.setText(Tipo);
            jt_correo.setText(correo);
            jt_cel.setText(cel_ref);
            jt_fi.setText(Fecha_i);
            jt_ff.setText(fecha_f);
        }
    }//GEN-LAST:event_jButtonSeleccionarActionPerformed

