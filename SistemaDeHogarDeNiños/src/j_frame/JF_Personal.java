    public void LogearPersonal(){
        switch(JF_principal.valiladorPrincipal){
            case"1":
                JF_Personal.jButtonNuevo.setVisible(false);
                JF_Personal.jButtonGuardar.setVisible(false);
                JF_Personal.jButtonEliminar.setVisible(false);
                JF_Personal.jButtonModificar.setVisible(false);
                JF_Personal.jButtonSeleccionar.setVisible(false);
                JF_Personal.jButton1.setVisible(false);
                break;
            case"2":
                JF_Personal.jButtonNuevo.setVisible(false);
                JF_Personal.jButtonGuardar.setVisible(false);
                JF_Personal.jButtonEliminar.setVisible(false);
                JF_Personal.jButtonModificar.setVisible(false);
                JF_Personal.jButtonSeleccionar.setVisible(false);
                JF_Personal.jButton1.setVisible(false);
                break;
            case"3":
            case"4":
                JF_Personal.jButtonNuevo.setVisible(false);
                JF_Personal.jButtonGuardar.setVisible(false);
                JF_Personal.jButtonEliminar.setVisible(false);
                JF_Personal.jButtonModificar.setVisible(false);
                JF_Personal.jButtonSeleccionar.setVisible(false);
                JF_Personal.jButton1.setVisible(false);
                break;
            default:
                break;               
        }
    }
